def main():
	#sum of two floating numbers
	float1 = 3.9
	float2 = 9.4
	sum_floats = float1 + float2
	print("Sum is ", sum_floats)
	print("Type of data is a ", type(sum_floats))

	#difference of two integers
	int1 = 4
	int2 = 1
	diff_ints = int1 - int2
	print("Difference is ", diff_ints)
	print("Type of data is a ", type(diff_ints))

	#product of a floating point number and an integer
	float3 = 8.45
	int3 = 5
	prod = float3 * int3
	print("Product is", prod)
	print("Type of data is a ", type(prod))


if __name__ == '__main__':
	main()
	
